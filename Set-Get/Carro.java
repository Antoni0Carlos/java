public class Carro{
    private String motor;
    private String cor;
    private String marca;

    public String getMotor(){
        return motor;
    }

    public void setMotor(String m){
        motor = m;
    }
    public String getCor(){
        return cor;
    }
    public String setCor(String c){
        cor = c;
    }
    public String getMarca(){
        return marca;
    }
    public void setMarca(String mc){
        marca = mc;
    }
}